+ (int)dayFromDate:(NSDate*)date
{
    NSCalendar *calendar = [NSCalendar currentCalendar];
    NSUInteger unitFlags =  NSCalendarUnitYear | NSCalendarUnitMonth | NSCalendarUnitDay | NSCalendarUnitHour | NSCalendarUnitMinute | NSCalendarUnitSecond;
    NSDateComponents *dateComponent = [calendar components:unitFlags fromDate:date];
    return (int)dateComponent.day;
    
}

+ (NSString *)dateIntToDateString:(double)dateDouble isList:(BOOL)isList
{
    
    NSDate *date = [NSDate dateWithTimeIntervalSince1970:dateDouble/1000.0];
    //转换规则
    
    NSDate *currentDate = [NSDate date];
    
    if (!(date && currentDate)) {
        return @"";
    }
    
    int today = [self dayFromDate:currentDate];
    int dateDay = [self dayFromDate:date];
    
    
    //用于格式化NSDate对象
    NSDateFormatter *dateFormatter = [[NSDateFormatter alloc] init];

    //NSDate转NSString
    NSString *resultDateString = nil;
    
    if (today == dateDay)
    {
        //显示 时分
        [dateFormatter setDateFormat:@"HH:mm"];
        resultDateString = [dateFormatter stringFromDate:date];
    }
    else if (today == (dateDay+1))
    {
        if (isList) {
            resultDateString = @"昨天";
        }
        else
        {
            [dateFormatter setDateFormat:@"HH:mm"];
            resultDateString = [NSString stringWithFormat:@"昨天 %@",[dateFormatter stringFromDate:date]];
        }
    }
    else{
        if (isList) {
            //resultDateString = @"昨天";
            [dateFormatter setDateFormat:@"yy/M/d"];
            resultDateString = [dateFormatter stringFromDate:date];
            
        }
        else
        {
            [dateFormatter setDateFormat:@"yy年M月d日 HH:mm"];
            resultDateString = [dateFormatter stringFromDate:date];
        }
    }
    
    return resultDateString;
}

